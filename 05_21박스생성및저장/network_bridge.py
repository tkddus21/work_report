    def recv_exactly(self, size):
        data = b''
        while len(data) < size:
            more = self.conn.recv(size - len(data))
            if not more:
                raise ConnectionError("Ïó∞Í≤∞ ÎÅäÍπÄ")
            data += more
        return data

    def connection_loop(self):
        while True:
            try:
                self.conn, addr = self.sock.accept()
                self.get_logger().info(f"[{CONTROL_PORT}] ÌÅ¥ÎùºÏù¥Ïñ∏Ìä∏ Ïó∞Í≤∞: {addr}")
                self.receive_loop()
            except Exception as e:
                self.get_logger().error(f"[ACCEPT ERROR] {e}")

